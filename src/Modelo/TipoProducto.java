package Modelo;

/**
 *
 * @author TIRESIA
 */
public class TipoProducto {
    
    private int id;
    private String nombre;
    
    public TipoProducto() {
    }
    

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    @Override
    public String toString() {
        return nombre;
    }
    
}
